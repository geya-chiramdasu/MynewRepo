public class EvenOrOdd {
    public static void main(String[] args) {
        int number = 29;

        // Checks if the remainder of division by 2 is zero
        if (number % 2 == 0) {
            System.out.println(number + " is an even number.");
        } else {
            System.out.println(number + " is an odd number.");
        }
    }
}
